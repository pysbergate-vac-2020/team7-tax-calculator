package com.psybergate.vacwork202006.web.controller;

public class RegisterDetailsController {
	private int taxNumber;
	private int salary;
	private int bonus;
	private int capitalGains;
	private int retirementFunding;
	private int travelAllowence;
	
	public int getTaxNumber() {
		return taxNumber;
	}
	public void setTaxNumber(int taxNumber) {
		this.taxNumber = taxNumber;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getBonus() {
		return bonus;
	}
	public void setBonus(int bonus) {
		this.bonus = bonus;
	}
	public int getCapitalGains() {
		return capitalGains;
	}
	public void setCapitalGains(int capitalGains) {
		this.capitalGains = capitalGains;
	}
	public int getRetirementFunding() {
		return retirementFunding;
	}
	public void setRetirementFunding(int retirementFunding) {
		this.retirementFunding = retirementFunding;
	}
	public int getTravelAllowence() {
		return travelAllowence;
	}
	public void setTravelAllowence(int travelAllowence) {
		this.travelAllowence = travelAllowence;
	}

	
}
